# Crear una matriz para almacenar el flujo de potencia aparente de cada lГӯnea activa por hora
S_flow_resultados = Dict(linea => [] for linea in [38, 43, 54, 56]);
# Inicializar arrays para almacenar las pГ©rdidas totales por hora
Ptot_real_por_hora = zeros(24);
Qtot_reactive_por_hora = zeros(24);
Stot_loss_por_hora = complex(zeros(24));
S_base = 10;  # Base de potencia en MVA

# Iterar por cada hora y calcular los flujos de potencia
for hora in 1:24
    # Extraer las magnitudes y fases para la hora actual
    magnitudes = magnitudes_horario[:, hora]
    fases = fases_horario[:, hora]
    
    # Filtrar las lГӯneas activas (con Estado == 1)
    T_lines_activas = T_lines[T_lines[:, 5] .== 1, :]
    
    # Calcular las pГ©rdidas, flujos y corrientes para la hora actual
    S_flow, corrientes_pu, corrientes_reales, Stot_loss, Ptot_real, Qtot_reactive, S_loss = calcular_flujos_y_perdidas_con_corrientes(T_lines_activas, magnitudes, fases, Buses, S_base)
    
    # Guardar los valores de S_flow para las lГӯneas de interГ©s en el diccionario
    for linea in [38, 43, 54, 56]
        pos_linea = findfirst(==(linea), T_lines_activas[:, 6])  # Encontrar la posiciГіn de la lГӯnea en T_lines_activas
        if pos_linea !== nothing  # Verificar si la lГӯnea estГЎ activa en esta hora
            push!(S_flow_resultados[linea], abs(S_flow[pos_linea]) * S_base)  # Guardar el valor escalado de S_flow
        else
            push!(S_flow_resultados[linea], 0.0)  # Si la lГӯnea no estГЎ activa, agregar un valor de 0.0
        end
    end
end

# Graficar los flujos de potencia aparente de las lГӯneas especГӯficas
plots_potencia_aparente_por_linea = [];
niveles_referencia = [5.0, 2.0, 4.0, 0.5];  # Niveles de referencia para saturaciГіn en MVA

# Iterar sobre cada lГӯnea de interГ©s para graficar
for (i, linea) in enumerate([38, 43, 54, 56])
    # Extraer los valores de potencia aparente guardados para esta lГӯnea
    S_apparent_por_hora = S_flow_resultados[linea]
    
    # Calcular el valor mГЎximo de la potencia aparente para ajustar el eje Y dinГЎmicamente
    max_potencia_aparente = maximum(S_apparent_por_hora)
    y_limit = max(max_potencia_aparente * 1.1, 2.5)  # LГӯmite de Y con un 10% de margen o 2.5 mГӯnimo
    
    # Crear la grГЎfica para esta lГӯnea a lo largo de las 24 horas
    p = plot(1:24, S_apparent_por_hora, 
        linecolor="blue",      # Color de la lГӯnea
        marker=:circle,        # Estilo de marcador
        markercolor="red",     # Color de los puntos (marcadores)
        markersize=3,          # TamaГұo de los marcadores
        label="LГӯnea $linea", 
        title="Flujo de Potencia Aparente - LГӯnea $linea", 
        xlabel="Hora del DГӯa", 
        ylabel="Potencia Aparente (MVA)", 
        lw=1,                  # Grosor de la lГӯnea
        xticks=1:2:24,         # Etiquetas para cada hora
        yticks=0:0.5:10,  # Etiquetas en el eje Y
        ylim=(0, 10),     # Limitar el eje Y dinГЎmicamente
        grid=true,             # Activar la cuadrГӯcula
        gridalpha=1.0,         # Transparencia de la cuadrГӯcula
        markerstrokewidth=0.2,
        tickfontsize=10,
        titlefontsize=10,
        ylabelfontsize=10,
        xlabelfontsize=10,
        gridcolor=:gray,
        legend=false, 
        size=(1000, 1000))     # TamaГұo de la grГЎfica
    
    # AГұadir la lГӯnea de referencia roja para el nivel de saturaciГіn
    hline!([niveles_referencia[i]], color=:red, lw=2, linestyle=:dash, alpha=0.8, label="")
    
    # Agregar la grГЎfica al array de grГЎficos
    push!(plots_potencia_aparente_por_linea, p)
end

# Mostrar las 4 grГЎficas, una para cada lГӯnea de interГ©s
p_final = plot(plots_potencia_aparente_por_linea..., layout=grid(2, 2), size=(1000, 1000))
