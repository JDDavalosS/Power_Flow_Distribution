# Calcular la potencia aparente a partir de la potencia activa y reactiva
S_slack_horario = [sqrt(P^2 + Q^2) for (P, Q) in zip(P_slack_horario, Q_slack_horario)]
# Crear el grГЎfico de la potencia activa
plot(1:24, P_slack_horario, 
    label="Potencia Activa (MW)", 
    linecolor="blue", 
    lw=2, 
    marker=:circle, 
    markersize=4,
    xlabel="Hora del DГӯa", 
    ylabel="Potencia (MW/MVAr/MVA)",
    title="Potencia del Bus Slack a lo largo del dГӯa",
    legend=:topleft, 
    grid=true,          # Activar la cuadrГӯcula
    gridcolor=:gray,    # Color de la cuadrГӯcula
    gridalpha=0.99)      # Transparencia de la cuadrГӯcula
# AГұadir la potencia reactiva al grГЎfico
plot!(1:24, Q_slack_horario, 
    label="Potencia Reactiva (MVAr)", 
    linecolor="red", 
    lw=2, 
    marker=:square, 
    markersize=4,
    grid=true)          # Reafirmar la cuadrГӯcula
# AГұadir la potencia aparente al grГЎfico
plot!(1:24, S_slack_horario, 
    label="Potencia Aparente (MVA)", 
    linecolor="green", 
    lw=2, 
    marker=:diamond, 
    markersize=4,
    grid=true)          # Reafirmar la cuadrГӯcula
# AГұadir una lГӯnea horizontal en el lГӯmite de 15 (en el eje Y)
hline!([15], color=:red, lw=2, linestyle=:dash, label=nothing)
