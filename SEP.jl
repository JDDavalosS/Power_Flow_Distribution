for hora in 1:24
    # Extraer las magnitudes y fases para la hora actual
    magnitudes = magnitudes_horario[:, hora]
    fases = fases_horario[:, hora]
    
    # Filtrar las lГӯneas activas (con Estado == 1)
    T_lines_activas = T_lines[T_lines[:, 5] .== 1, :]
    
    # Calcular las pГ©rdidas, flujos y corrientes para la hora actual
    S_flow, corrientes_pu, corrientes_reales, Stot_loss, Ptot_real, Qtot_reactive, S_loss = calcular_flujos_y_perdidas_con_corrientes(T_lines_activas, magnitudes, fases, Buses, S_base)
    
    # Guardar las pГ©rdidas reales y reactivas en los arrays correspondientes
    Ptot_real_por_hora[hora] = Ptot_real
    Qtot_reactive_por_hora[hora] = Qtot_reactive
    
    # Imprimir los resultados de pГ©rdidas por cada hora
    println("Hora $hora:")
    println("PГ©rdidas totales reales de potencia: ", round(Ptot_real, digits = 9), " MW")
    println("PГ©rdidas totales reactivas de potencia: ", round(Qtot_reactive, digits = 9), " MVAr")
    println("PГ©rdidas totales aparentes de potencia: ", round(Stot_loss, digits = 9), " MVA")
    println("Potencia real suministrada por el bus slack: ", P_slack_horario[hora], " MW")
    println("Potencia reactiva suministrada por el bus slack: ", Q_slack_horario[hora], " MVAr")
    
    # Imprimir los flujos de potencia real, reactiva y aparente por lГӯnea, con los buses de origen y destino solo para lГӯneas activas
    println("Flujos de potencia en cada lГӯnea activa:")
    for i in 1:size(T_lines_activas, 1)
        bus_origen = T_lines_activas[i, 1]
        bus_destino = T_lines_activas[i, 2]
        
        # Calcular la potencia real, reactiva y aparente
        P_real = (real(S_flow[i])) * S_base  # Potencia real (MW)
        Q_reactive = imag(S_flow[i]) * S_base  # Potencia reactiva (MVAr)
        S_apparent = sqrt((P_real)^2 + (Q_reactive)^2)  # Potencia aparente (MVA)
        
        # Imprimir los resultados para la lГӯnea `i` junto con los nodos de origen y destino
        println("LГӯnea $(T_lines_activas[i, 6]) (Bus $bus_origen -> Bus $bus_destino):")
        println("    Potencia real: ", round(P_real, digits = 9), " MW")
        println("    Potencia reactiva: ", round(Q_reactive, digits = 9), " MVAr")
        println("    Potencia aparente: ", round(S_apparent, digits = 9), " MVA")
        println("    Corriente real: ", round(corrientes_reales[i], digits = 9), " A")
    end
    
    # Imprimir las corrientes en cada lГӯnea activa (en p.u. y reales), con los buses de origen y destino
    println("Corrientes en cada lГӯnea activa:")
    for i in 1:size(T_lines_activas, 1)
        bus_origen = T_lines_activas[i, 1]
        bus_destino = T_lines_activas[i, 2]
        
        println("LГӯnea $(T_lines_activas[i, 6]) (Bus $bus_origen -> Bus $bus_destino): Corriente real: ", round(corrientes_reales[i], digits = 9), " A")
    end
