#---FunciГіn para calcular el flujo de potencia en funciГіn de la demanda, generaciГіn solar, eГіlica y baterГӯas--------#
function calcular_flujo_carga_horario(Buses, T_lines, demand_profile, P_nom, g_location, pv_gen, fp, wind_location, Pw2_out, fp_wind, battery_location, battery_capacity, charge_hours, discharge_hours, Ybus, Sbase, electrostation_location, carga_diaria)
    resultados_magnitudes = [];
    resultados_fases = [];
    resultados_P_slack = [];
    resultados_Q_slack = [];
    
    for hora in 1:24
        # Copiar y ajustar la demanda para esta hora segГәn el perfil de demanda
        Buses_hora = copy(Buses)
        
        # Ajuste de la demanda en cada bus segГәn el perfil de demanda
        for i in 1:size(Buses, 1)
            Buses_hora[i, 5] *= demand_profile[hora]  # Ajuste de Pdem segГәn el perfil de demanda
            Buses_hora[i, 6] *= demand_profile[hora]  # Ajuste de Qdem segГәn el perfil de demanda
        end
        
        # AГұadir la carga de las electrolineras para esta hora
        for (i, bus) in enumerate(electrostation_location)
            Buses_hora[bus, 5] += carga_diaria[hora, i] / 1000  # Convertir kW a MW y agregar al Pdem del bus correspondiente
        end

        # AГұadir generaciГіn fotovoltaica en los buses especificados
        for (generation, bus) in enumerate(g_location)
            Pgen = P_nom[generation] / 1000 * pv_gen[hora]  # Convertir kW a MW y ajustar por el perfil horario
            Buses_hora[bus, 3] += Pgen  # Sumar la potencia activa generada fotovoltaica
            Qgen = Pgen * tan(acos(fp[generation]))  # Calcular y asignar la potencia reactiva generada fotovoltaica
            Buses_hora[bus, 4] += Qgen
        end
        
        # AГұadir generaciГіn eГіlica en los buses especificados
        for (i, bus) in enumerate(wind_location)
            Pgen = Pw2_out[hora, i] / 1000  # Convertir W a MW
            Buses_hora[bus, 3] += Pgen  # Sumar la potencia activa generada eГіlica
            Qgen = Pgen * tan(acos(fp_wind[i]))  # Calcular y asignar la potencia reactiva generada eГіlica
            Buses_hora[bus, 4] += Qgen
        end
        
        # Ajuste del comportamiento de las baterГӯas en los buses
        for (idx, bus) in enumerate(battery_location)
            if hora in charge_hours
                # Durante las horas de carga, las baterГӯas actГәan como una carga
                carga_bateria = battery_capacity[idx] / 1000  # Convertir kW a MW
                Buses_hora[bus, 5] += carga_bateria  # Agregar carga en Pdem del bus
            elseif hora in discharge_hours
                # Durante las horas de descarga, las baterГӯas actГәan como generaciГіn
                generacion_bateria = battery_capacity[idx] / 1000  # Convertir kW a MW
                Buses_hora[bus, 3] += generacion_bateria  # Agregar generaciГіn en Pgen del bus
            end
        end
        
        # Calcular el flujo de carga para la configuraciГіn ajustada
        magnitudes, fases = calcular_flujo_carga(Buses_hora, T_lines, Sbase)
        
        # Almacenar los resultados de magnitudes y fases
        push!(resultados_magnitudes, magnitudes[:, end])
        push!(resultados_fases, fases[:, end])
        
        # Convertir magnitudes y fases a formato rectangular para obtener Vbus
        Vbus = magnitudes[:, end] .* exp.(im * fases[:, end] * pi / 180)
        
        # Calcular la potencia del bus slack
        P_slack, Q_slack = calcular_potencia_slack(Buses_hora, Ybus, Vbus, Sbase)
        push!(resultados_P_slack, P_slack)
        push!(resultados_Q_slack, Q_slack)
    end 
    
    # Retornar los resultados organizados en matrices
    return hcat(resultados_magnitudes...), hcat(resultados_fases...), resultados_P_slack, resultados_Q_slack
end
# Llamada a la funciГіn incluyendo las potencias eГіlicas (Pw2_out), generaciГіn solar (pv_gen) y baterГӯas
magnitudes_horario, fases_horario, P_slack_horario, Q_slack_horario = calcular_flujo_carga_horario(Buses, T_lines, demand_profile, P_nom, g_location, pv_gen, fp, wind_location, Pw2_out, fp_wind,
battery_location, battery_capacity, charge_hours, discharge_hours, Ybus, Sbase, electrostation_location, carga_diaria)
