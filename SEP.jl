#-----------------------------------Grafica de los voltajes cada hora en cada bus----------------------------------#



# NГәmero de buses en el sistema
n_buses = size(magnitudes_horario, 1);
# Inicializar un array para almacenar las grГЎficas de voltajes por hora
plots_voltajes = [];
# NГәmero de repeticiones
n_repeticiones = 1;  # Este nГәmero controla cuГЎntas veces se repiten las grГЎficas
# Ciclo para mostrar las grГЎficas de voltajes por bus en cada hora
for _ in 1:n_repeticiones  
    for hora in 1:24
        # Graficar los voltajes en todos los buses para la hora actual
        p = plot(1:n_buses, magnitudes_horario[:, hora], 
            linecolor="green",      # Color de la lГӯnea
            marker=:circle,         # Estilo de marcador
            markercolor="blue",     # Color de los puntos (marcadores)
            label="Hora $hora", 
            title="Voltaje (p.u) - Hora $hora", 
            #ylabel="Voltaje (p.u.)", 
            #xlabel=:, 
            lw=1,                   # Grosor de la lГӯnea
            #legend=:topleft,
            xticks=1:3:n_buses,     # Etiquetas para cada bus
            yticks=0.75:0.03:1.02,    # Ajusta los lГӯmites del eje Y segГәn los valores de voltaje esperados
            ylim=(0.75, 1.02),        # Establecer los lГӯmites del eje Y
            grid=true,              # Activar la cuadrГӯcula
            gridalpha=0.99,         # Transparencia de la cuadrГӯcula
            markerstrokewidth=0.01,
            labelpadding=-10,      # Sirve para ajustar la distancia entre las etiquetas y los ejes
            tickfontsize=10,
            gridcolor=:gray,
            legend=false,
            size=(1000, 900)) 
             # Guardar la grГЎfica en el array
              push!(plots_voltajes, p)     # Establecer el tamaГұo de la grГЎfica
        # Mostrar la grГЎfica de la hora actual
        display(p)        
        # Pausa opcional para visualizar cada grГЎfica antes de continuar con la siguiente
        sleep(0.1)  # Pausar por 2 segundos antes de pasar a la siguiente grГЎfica 
    end
end
# Mostrar las 24 grГЎficas en una sola ventana
plot(plots_voltajes..., layout=(8, 3), size=(2000, 2000), subplot_padding=1)  # 24 filas, 1 columna para acomodar las 24 grГЎficas

savefig("VOLTAJES_BESS_DGU.pdf")  # Guardar el archivo



