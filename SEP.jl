# FunciГіn para calcular la matriz de admitancia: recordemls que la nomenclatura utilizada es que se utilizaran los datos de la matriz de T_lines 
# que son numeros flotantes con presicion de dos dimensiones (filas y  columnas)...Y los dos puntos sirven para restringir que solo se utilice un arreglo de dos dimensiones para esta funcion.
function Admittance_matrix_JDDS(T_lines::Array{Float64,2})
    n = Int(maximum(T_lines[:, 1:2]))  
# Convierte el nГәmero mГЎximo de nodos a entero
# T_lines[:, 1:2]: selecciona todas las filas mediante ;V) de las primeras dos columnas usando (1:2) de la matriz T_lines. En el contexto de la 
# matriz de datos de lГӯneas de transmisiГіn, estas dos columnas representan los nodos de inicio y fin de cada lГӯnea, respectivamente.
# Inicializa la matriz Ybus con ceros, usando complejos para admitir partes imaginarias
    Ybus = zeros(Complex{Float64}, n, n)    
# Procesa cada lГӯnea para actualizar los elementos de Ybus
        for linea in eachrow(T_lines)
# Convierte los Гӯndices a enteros para evitar el error de Гӯndice invГЎlido
            nodo_inicio = Int(linea[1])
            nodo_fin = Int(linea[2])
             R, X, estado = linea[3:5]
# Verifica si la lГӯnea estГЎ en servicio
            if estado == 1
                admitancia = 1 / (R + X*im)  
# Usa im directamente para la parte imaginaria        
# Actualiza los elementos correspondientes en la matriz Ybus
                   Ybus[nodo_inicio, nodo_inicio] += admitancia # += Actualiza el valor de la variable mediante una suma...
                   Ybus[nodo_fin, nodo_fin] += admitancia
                   if nodo_inicio != nodo_fin  
# Asegura que no se actualice dos veces para bucles
                        Ybus[nodo_inicio, nodo_fin] -= admitancia # -= se utiliza para actualizar el valor de una variable mediante una resta...
                       Ybus[nodo_fin, nodo_inicio] -= admitancia
                   end
            end
        end
        return Ybus
end
