#----------------------------CALCULAR_PERDIDAS_Y_FLUJOS_DE_POTENCIA_Y_CORRIENTES------------------------------------#
function calcular_flujos_y_perdidas_con_corrientes(T_lines, magnitudes_horario, fases_horario, Buses, S_base)
    # Filtrar las lГӯneas activas
    T_lines_activas = T_lines[T_lines[:, 5] .== 1, :]
    # NГәmero de lГӯneas de transmisiГіn activas
    n_lt = size(T_lines_activas, 1)
    # Extraer informaciГіn de los buses iniciales y finales
    global bus_from = Int.(T_lines_activas[:, 1])
    global bus_to = Int.(T_lines_activas[:, 2])
    # Convertir voltajes de polar a rectangular (magnitud y fase)
    Vbus = [magnitudes_horario[h] * exp(im * deg2rad(fases_horario[h])) for h in 1:length(magnitudes_horario)]
    # Inicializar vectores para almacenar las pГ©rdidas, flujos de potencia y corrientes
    S_loss = complex(zeros(n_lt))
    S_flow = complex(zeros(n_lt))
    corrientes_pu = complex(zeros(n_lt))  # Corrientes en p.u.
    corrientes_reales = zeros(n_lt)  # Corrientes reales
    Ptot_real = 0.0
    Qtot_reactive = 0.0
    Stot_loss = complex(0.0)
    for h in 1:n_lt
        # Calcular la impedancia de la lГӯnea de transmisiГіn
        Z_tl = T_lines_activas[h, 3] + T_lines_activas[h, 4] * im
        # Calcular la corriente en p.u.
        I_pu = (Vbus[bus_from[h]] - Vbus[bus_to[h]]) / Z_tl
        corrientes_pu[h] = I_pu  # Almacenar la corriente en p.u.
        # Calcular el flujo de potencia aparente
        S_flow[h] = Vbus[bus_from[h]] * conj(I_pu)
        # Calcular la corriente base para la lГӯnea (en funciГіn del voltaje base del bus de origen)
        V_base_from_bus = (Buses[bus_from[h], 9]) / 1000  # KVBASE correspondiente al bus de origen RECUERDA QUE ESTAN EL kV, asi que debes de convertirlos en MV para hacer el calculo de la corriente base Ibase...
        I_base = S_base / (sqrt3 * V_base_from_bus)  # Corriente base en A
        # Convertir la corriente en p.u. a corriente real
        corrientes_reales[h] = abs(I_pu) * I_base
        # Calcular las pГ©rdidas reales y reactivas
        P_loss_real = (abs(I_pu)^2 * real(Z_tl)) * S_base
        Q_loss_reactive = (abs(I_pu)^2 * imag(Z_tl)) * S_base
        # Calcular las pГ©rdidas de potencia aparente
        S_loss[h] = P_loss_real + Q_loss_reactive * im
        # Acumular las pГ©rdidas totales
        Stot_loss += S_loss[h]
        Ptot_real += P_loss_real
        Qtot_reactive += Q_loss_reactive
    end
    # Retornar los flujos de potencia, las corrientes en p.u. y reales, las pГ©rdidas totales aparentes, reales y reactivas
    return S_flow, corrientes_pu, corrientes_reales, Stot_loss, Ptot_real, Qtot_reactive, S_loss
end



