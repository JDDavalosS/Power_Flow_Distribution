#-------------------------------CALCULAR LOS VOLTAJES DEL SISTEMA (FLUJO DE CARGA)----------------------------------#
function calcular_flujo_carga(Buses, T_lines, Sbase)
    Ybus = Admittance_matrix_JDDS(T_lines)
    #global Sbase = 10 REXORDAR QUE LA DEFINIMOS ANTERIORMENTE PARA USARLA ENTODO EL CODIGO NO SOLO EN LA FUNCION (PROBLEMAS CON "GLOBAL")
    alfa = 1
    error0 = 1e-6
    maxIter = 15000
    global Numero_buses = length(Buses[:,1])
    Pgenerada = Buses[:,3] / Sbase
    Qgenerada = Buses[:,4] / Sbase
    Pdemandada = Buses[:,5] / Sbase
    Qdemandada = Buses[:,6] / Sbase
    P_esperada = Pgenerada - Pdemandada
    Q_esperada = Qgenerada - Qdemandada
    Vbus0 = complex(zeros(Numero_buses,1))
    Vbus = complex(zeros(Numero_buses,1))
    Vbus_calculado = complex(zeros(Numero_buses,1))
    for r in 1:Numero_buses
        Vbus0[r] = (Buses[r,7]) * (cos(Buses[r,8]*pi/180)) + (Buses[r,7]) * (sin(Buses[r,8]*pi*im/180))
    end
    Vbus_h = complex(zeros(Numero_buses, maxIter))
    Vbus = Vbus0
    Vbus_h[:,1] = Vbus0
    error = 1
    global iter = 0
    DV = complex(zeros(Numero_buses,1))
    while iter < maxIter && error > error0
        global iter += 1
        for r in 1:Numero_buses
            if Buses[r,2] == 1
                # Slack bus
            elseif Buses[r,2] == 3
                Isum = 0
                for m in 1:Numero_buses
                    if m != r
                        Isum += Ybus[r,m] * Vbus[m]
                    end
                end
                Vbus_calculado[r] = (((P_esperada[r] - Q_esperada[r]*im) / conj(Vbus[r])) - Isum) / Ybus[r,r]
                Vbus[r] = Vbus[r] + alfa * (Vbus_calculado[r] - Vbus[r])
            elseif Buses[r,2] == 2
                Isum = 0
                for m in 1:Numero_buses
                    if m != r
                        Isum += Ybus[r,m] * Vbus[m]
                    end
                end
                Q_esperada[r] = -imag(conj(Vbus[r]) * ((Ybus[r,r] * Vbus[r]) + Isum))
                Vbus_calculado[r] = (((P_esperada[r] - Q_esperada[r]*im) / conj(Vbus[r])) - Isum) / Ybus[r,r]
                Vbus[r] = (abs(Vbus[r]) / abs(Vbus_calculado[r])) * Vbus_calculado[r]
            end
        end
        if iter > 1
            Vbus_h[:,iter] = Vbus
            global DV = Vbus_h[:,iter] - Vbus_h[:,iter-1]
        else
            global DV = Vbus - Vbus_h[:,iter]
        end
        global error = maximum(abs.(DV))
    end
     # Devolver tanto la magnitud como la fase de los voltajes
     magnitudes = abs.(Vbus_h[:,1:iter])
     fases = angle.(Vbus_h[:,1:iter]) * 180 / pi  # Convertir las fases a grados
    return magnitudes, fases
end

