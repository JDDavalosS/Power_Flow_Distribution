#---------------------------Funcion para determinar la carga segun el tipo de vehiculo y temporada----------------------#
function calcular_carga_estaciones_diaria(tipo_dia, temporada, tipo_vehiculo_por_estacion)
    carga_diaria = zeros(24, length(electrostation_location))
    
    for i in 1:length(electrostation_location)
        vehiculo = tipo_vehiculo_por_estacion[i]
        
        # ValidaciГіn del tipo de vehГӯculo
        if !haskey(perfiles_carga, vehiculo)
            error("Error: El tipo de vehГӯculo '$vehiculo' no es vГЎlido. Use: 'sedan', 'autobus', 'van' o 'troka'.")
        end

        # SelecciГіn del perfil adecuado
        perfil = perfiles_carga[vehiculo][temporada][tipo_dia]
        
        # MultiplicaciГіn del perfil por la potencia mГЎxima de carga
        carga_diaria[:, i] = perfil .* max_charge_power[i]
    end
    
    return carga_diaria
end

