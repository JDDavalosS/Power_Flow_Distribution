#-----------------------------------Grafica de flujo de potencia aparente----------------------------------#
# Obtener el nГәmero mГЎximo de la lГӯnea conectada para el eje X
max_connected_line = Int(maximum(T_lines[T_lines[:, 5] .== 1, 6]));
# Inicializar un array para almacenar las grГЎficas del flujo de potencia aparente por hora
plots_potencia_aparente = []
# Iterar por cada hora y calcular el flujo de potencia aparente
for hora in 1:24
    # Extraer las magnitudes y fases para la hora actual
    magnitudes = magnitudes_horario[:, hora]
    fases = fases_horario[:, hora]
    # Calcular las pГ©rdidas, flujos y corrientes para la hora actual
    S_flow, corrientes_pu, corrientes_reales, Stot_loss, Ptot_real, Qtot_reactive, S_loss = calcular_flujos_y_perdidas_con_corrientes(T_lines, magnitudes, fases, Buses, S_base)
    # Inicializar un array para la potencia aparente incluyendo las lГӯneas desconectadas
    S_apparent_totales = zeros(size(T_lines, 1))  # Crear un array de ceros con el tamaГұo de todas las lГӯneas
    # Llenar con los valores de la potencia aparente solo para las lГӯneas conectadas
    index_flow = 1  # Гӯndice para recorrer `S_flow`
    for i in 1:size(T_lines, 1)
        if T_lines[i, 5] == 1.0  # Si la lГӯnea estГЎ conectada
            S_apparent_totales[i] = abs(S_flow[index_flow]) * S_base  # Asigna la magnitud de la potencia aparente
            index_flow += 1  # Incrementa el Гӯndice de `S_flow`
        else
            S_apparent_totales[i] = 0.0  # Si estГЎ desconectada, deja en 0
        end
    end
    
    # Crear la grГЎfica para la potencia aparente por lГӯnea en la hora actual, limitando el eje X
    p = plot(1:max_connected_line, S_apparent_totales[1:max_connected_line], 
        linecolor="green",      # Color de la lГӯnea
        marker=:circle,         # Estilo de marcador
        markercolor="red",      # Color de los puntos (marcadores)
        markersize=3,           # TamaГұo de los marcadores
        label="Hora $hora", 
        title="Flujo Potencia Aparente MVA - Hora $hora", 
        lw=1,                   # Grosor de la lГӯnea
        xticks=1:4:max_connected_line,  # Etiquetas para cada lГӯnea
        yticks=0:2:18,          # Ajustar segГәn los valores mГЎximos
        ylim=(0, 18),           # Ajustar los lГӯmites del eje Y
        grid=true,              # Activar la cuadrГӯcula
        gridalpha=0.99,         # Transparencia de la cuadrГӯcula
        markerstrokewidth=0.03,
        labelpadding=-10,       # Ajustar la distancia entre las etiquetas y los ejes
        tickfontsize=10,
        gridcolor=:gray,
        legend=false, 
        size=(1000, 900))       # TamaГұo de la grГЎfica
    
    # Agregar la grГЎfica al array de grГЎficos
    push!(plots_potencia_aparente, p)
end

# Mostrar todas las 24 grГЎficas en una ventana con un layout de 6 filas y 4 columnas
plot(plots_potencia_aparente..., layout=(6, 4), size=(2000, 2000))
# Guardar el archivo
savefig("FLUJO_APARENTE_BESS_DGU.pdf")



