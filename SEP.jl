# Definir la funciГіn que lee el perfil de viento y calcula la potencia eГіlica para mГәltiples turbinas
function calcular_potencia_eolica(archivo_csv::String, D::Vector{Float64}, Pmax::Vector{Float64}, Vc::Vector{Float64}, Vr::Vector{Float64}, Vf::Vector{Float64})
    # Leer los datos del archivo CSV que contiene las velocidades del viento
    data = CSV.read("Oklahoma.csv", DataFrame)
    # Asumimos que las columnas son: la primera columna es 'h' (hora) y la segunda es 'velocidades'
    global h = data[:, 1];  # Hora
    global velocidades = data[:, 2];  # Velocidades del viento
    # Inicializar vectores para cada turbina
    nf = length(h)
    Pw2_out = zeros(nf, length(Pmax))  # Potencias eГіlicas para cada hora y para cada turbina
    # ParГЎmetros generales del viento
    rho = 1.225  # Densidad del aire en kg/m^3
    Cp = 16/27  # Coeficiente de potencia (LГӯmite de Betz)
    # CГЎlculo de potencia generada por los generadores eГіlicos basado en la velocidad del viento
    for k in 1:nf
        velk = velocidades[k]  # Velocidad del viento en la hora k
        for i in 1:length(Pmax)  # Para cada turbina eГіlica (ubicaciГіn)
            A = ПҖ * (D[i] / 2)^2  # ГҒrea del rotor para la turbina en la ubicaciГіn i
            if velk < Vc[i]
                Pw2_out[k, i] = 0.0
            elseif velk >= Vc[i] && velk <= Vr[i]
                vm = (velk - Vc[i]) / (Vr[i] - Vc[i])
                Pw2_out[k, i] = (1/2) * Cp * rho * A * (vm^3)
                if Pw2_out[k, i] > Pmax[i]  # Limitar a la potencia mГЎxima
                    Pw2_out[k, i] = Pmax[i]
                end
            elseif velk > Vr[i] && velk <= Vf[i]
                Pw2_out[k, i] = Pmax[i]  # Si la velocidad es mayor o igual a nominal, generar potencia mГЎxima
            elseif velk > Vf[i]
                Pw2_out[k, i] = 0.0  # Si supera la velocidad de corte superior, no genera potencia
            end
        end
    end
    # Retornar el perfil de potencia generada por las turbinas eГіlicas
    return Pw2_out
end
