#----------------------------------------CALCULAR_POTENCIAS_DEL_BUS_SLACK-------------------------------------------#
function calcular_potencia_slack(Buses, Ybus, Vbus, Sbase)
    # Voltaje en el bus slack
    V_slack = Vbus[1]  
    # Calcular la corriente inyectada en el bus slack
    I_slack = sum(Ybus[1, :] .* Vbus)
    # Calcular la potencia compleja generada en el bus slack
    S_slack = Sbase * V_slack * conj(I_slack)
    # Separar la potencia real y reactiva
    P_slack = real(S_slack)  # Potencia real en MW
    Q_slack = imag(S_slack)  # Potencia reactiva en MVAr 
    return P_slack, Q_slack
end




